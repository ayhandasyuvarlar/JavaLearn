    public static void main(String[] args) {
        int ix,j,kx;
        for (ix=8; 0<ix; ix--) {
            for(j=ix; j<8;j++) {
                System.out.print(" ");
            for (kx=1; kx<=ix; kx++) {
                System.out.print(" ");
            }
            System.out.println(" ");
        }


        }



    }

